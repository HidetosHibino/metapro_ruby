# ブロックの基本

def a_method(a, b)
  a + yield(a, b)
end

p a_method(1, 2) { |x, y| (x + y) * 3 }

# ブロックは波括弧または do...end キーワードで定義できる。
# 1行のブロックには波括弧を使い、複数行のブロックにはdo...endを使う
# 「ブロックを呼び出すのはメソッドを呼び出すときだけ」
# ブロックはメソッドに渡され、メソッドはyield キーワードを使ってブロックをコールバックする。
# ブロックは任意で引数を受け取れる。上記のx,yがそう。
# ブロックをコールバックするときは、メソッドを呼び出す時と同じように引数を渡せる。

# メソッドの内部では、Kernel#block_given? メソッドを使ってブロックの有無を確認できる。

def b_method
  return yield if block_given?

  'ブロックがありません'
end

p b_method
p b_method { 'ブロックがある' }
